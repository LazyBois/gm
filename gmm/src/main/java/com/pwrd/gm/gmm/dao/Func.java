package com.pwrd.gm.gmm.dao;


public class Func {

  private long id;
  private long gameid;
  private String name;
  private long type;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getGameid() {
    return gameid;
  }

  public void setGameid(long gameid) {
    this.gameid = gameid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getType() {
    return type;
  }

  public void setType(long type) {
    this.type = type;
  }

}
